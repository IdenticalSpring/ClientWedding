function useDemoRouter() {
  const navigate = useNavigate(); // Replace useHistory with useNavigate
  const location = useLocation(); // Use useLocation for pathname

  return {
    pathname: location.pathname, // Access current pathname
    searchParams: new URLSearchParams(location.search), // Access search params
    navigate: (path) => navigate(path), // Use navigate instead of history.push
  };
}

